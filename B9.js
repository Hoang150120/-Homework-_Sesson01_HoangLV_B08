let a = 5;
let b = "5";

console.log(a==b);
console.log(a===b);
console.log(a!=b);
console.log(a!==b);

//khi có giá trị a=5, b="5" thì:
//khi a==b có kết quả là true thể hiện rằng a bằng b
//khi a===b có kết quả là false thể hiện rằng a không bằng b
//khi a!=b có kết quả là false thể hiện rằng a khác b
//khi a!==b có kết quả là true thể hiện rằng a giống b
